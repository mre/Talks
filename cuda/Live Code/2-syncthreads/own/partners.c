#include <stdlib.h>
#include <stdio.h>

/*


0,3    1,2


2,1    3,0

0000 ^ 0010 => 0010   2
0001 ^ 0010 => 0011   3
0010           0000   0
0011           0001   1

0      1
|      |
|      |
2      3

---

0000 ^ 0001 => 0001   1
0001 ^ 0001 => 0000   0
0010           0011   3
0011           0010   2

0------1


2------3

*/

int main(void)
{
  int i, j;
  /* Major step */
  for (j=2; j>0; j=j>>1) {
    for (i = 0; i < 4; ++i) {
      int ixj = i^j;
      printf("%d, %d\n", i, ixj);
    }
  }
}
